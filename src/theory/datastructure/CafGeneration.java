package theory.datastructure;

import caf.datastructure.Caf;

public class CafGeneration {
    private Caf caf1;
    private Caf caf2;

    public CafGeneration(Caf caf1, Caf caf2) {
        this.caf1 = caf1;
        this.caf2 = caf2;
    }

    public Caf getCaf1() {
        return caf1;
    }

    public Caf getCaf2() {
        return caf2;
    }
}
